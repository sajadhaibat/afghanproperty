    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $file = $request->file('banner');
        $filename = time().'_'.$file->getClientOriginalName();
        $path = public_path().'/images';
        $file->move($path,$filename);

        $land = new Land;
        $land->name=$request->label;
        $land->area=$request->area;
        $land->location=$request->location;
        $land->price=$request->price;

        $land->number=$request->number;
        $land->description=$request->description;
        $land->banner=$filename;
        $land->save();
        return redirect(route('land.index'));

    }

